                <p><strong>Amoxicillin:</strong> This is the preferred first-line antibiotic for Lyme disease during pregnancy. Amoxicillin is a penicillin-type antibiotic that is very safe during pregnancy (FDA Category B‚Äîno evidence of fetal risk in studies). Typical dosing is 500 mg three times daily for 21 days for early Lyme disease, or longer for disseminated disease. Amoxicillin has excellent penetration into the central nervous system, making it effective for Lyme neuroborreliosis.</p>

