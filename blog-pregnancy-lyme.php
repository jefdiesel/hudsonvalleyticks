                <h2>Testing and Diagnosis During Pregnancy</h2>

                <p>If you're pregnant and believe you may have Lyme disease, testing and diagnosis are important but should be done carefully, considering the unique context of pregnancy. Early diagnosis allows for prompt, appropriate treatment that protects both you and your baby.</p>

                <h3>When to Test for Lyme Disease in Pregnancy</h3>

                <p>Test for Lyme disease if you have: a known tick bite or tick exposure (particularly with removal delay), flu-like symptoms during pregnancy, the characteristic bull's-eye rash (erythema migrans), or symptoms compatible with Lyme disease (fever, fatigue, joint pain, headache) during tick season in endemic areas.</p>

                <p>Also consider testing if you're experiencing unexplained symptoms during pregnancy that might be Lyme disease: persistent fatigue beyond normal pregnancy fatigue, joint pain (not typical of pregnancy), neurological symptoms, heart palpitations, or fever without obvious cause.</p>

                <h3>Testing Methods and Accuracy in Pregnancy</h3>

                <p>The two-tier testing protocol recommended by CDC is appropriate in pregnancy. Initial testing uses an ELISA (enzyme-linked immunosorbent assay) test to detect antibodies against Borrelia burgdorferi. If positive, confirmatory testing uses Western blot. This two-tier approach minimizes false positives.</p>

                <p>Pregnancy does not affect the accuracy of Lyme disease tests, though the immune changes of pregnancy can rarely affect antibody levels. Testing is safe during pregnancy and should not be delayed.</p>

                <p>Importantly, pregnant women with tick exposure should be tested even without symptoms. A negative test when Lyme disease is actually in the window period (first 3-4 days of infection before antibodies develop) can be falsely reassuring. If you had tick exposure and symptoms develop later, repeat testing after 4-6 weeks.</p>

                <h3>Early Treatment Window</h3>

                <p>The absolute best outcome occurs with treatment immediately after tick exposure or when the characteristic early rash develops. If diagnosed at this stage, a single dose of azithromycin or 2-3 weeks of amoxicillin essentially eliminates the risk of Lyme disease progression. This is why identification of tick exposure and early recognition of erythema migrans is so valuable.</p>
