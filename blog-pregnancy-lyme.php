                <h2>Vertical Transmission: Can Lyme Disease Be Passed to Your Baby?</h2>

                <p>This is the question that worries pregnant women most: Can my baby be born with Lyme disease? The answer, based on current evidence, is yes, but very rarely. Transplacental transmission of Borrelia burgdorferi does occur, but it is uncommon and doesn't necessarily result in congenital Lyme disease or serious complications.</p>

                <h3>How Vertical Transmission Works</h3>

                <p>If a mother has active Lyme disease during pregnancy, particularly if untreated, the spirochete can cross the placental barrier and infect the fetus. This is called transplacental transmission. However, the majority of infected fetuses clear the infection spontaneously, and most do not develop symptomatic congenital Lyme disease.</p>

                <p>When congenital Lyme disease does develop, it typically presents with non-specific manifestations: prematurity, rash, hepatosplenomegaly (enlarged liver and spleen), jaundice, or cognitive impairment. True congenital Lyme disease (comparable to congenital syphilis or congenital rubella) is extraordinarily rare in the United States‚Äîonly a handful of cases have been confirmed in the medical literature despite millions of pregnancies in Lyme-endemic areas.</p>

                <h3>Risk Factors for Transmission</h3>

                <p>The risk of vertical transmission appears to be highest when the mother has active disseminated Lyme disease during pregnancy, particularly if untreated. First-trimester infection carries somewhat higher risk than later trimester infection. Lyme disease with significant bacteremia (spirochetes in the bloodstream) poses greater transmission risk than localized disease.</p>

                <p>The reassuring news: maternal treatment with appropriate antibiotics dramatically reduces the risk of transmission and virtually eliminates the risk of symptomatic congenital Lyme disease. Most cases of confirmed congenital Lyme disease occurred when the mother had untreated Lyme disease during pregnancy.</p>

                <h3>Breast Feeding Considerations</h3>

                <p>Lyme spirochetes have not been detected in breast milk. Breast feeding is safe for mothers with Lyme disease, whether treated or untreated. In fact, breast feeding may provide passive immunity to the infant through antibodies in breast milk. Continue breast feeding even while taking antibiotics for Lyme disease‚Äîthe antibiotics used (doxycycline after pregnancy, amoxicillin during pregnancy) are safe with breast feeding or are minimally excreted into breast milk.</p>

