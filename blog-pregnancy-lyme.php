                <p>Reduce tick exposure around your home and yard. Mow grass short, remove leaf litter and brush from areas where your family spends time, create a barrier of wood chips or mulch to separate lawn from wooded areas, and consider tick tubes or yard treatment (discuss safety with your doctor if considering chemical treatments during pregnancy).</p>

