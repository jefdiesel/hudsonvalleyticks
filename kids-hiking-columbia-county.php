        <p>Dress children in light-colored long pants (white or khaki), long sleeves, and closed-toe hiking boots. Apply permethrin to clothing the night before‚Äîspray outdoors and allow 1‚Äì2 hours for drying. Choose permethrin-treated hiking clothing for maximum protection without additional application.</p>

        <h3>During the Hike</h3>
        <p>Stay on marked trails‚Äîhiking off-trail significantly increases tick exposure. Tuck pants into socks and tuck shirts into waistbands to create barriers. Perform quick visual tick checks every 30 minutes, paying special attention to lower legs and ankles. Even though permethrin-treated clothing repels ticks, visual inspection provides additional security.</p>

        <h3>Post-Hike Protocol</h3>
        <p>Change clothes immediately upon returning home. Wash all hiking clothes in hot water. Inspect children thoroughly‚Äîremove any found ticks using fine-point tweezers, grasping close to the skin and pulling steadily upward. Shower within 2 hours if possible.</p>

