<section tabindex="0" id="three-main-ticks">
    <h2>The Three Main Ticks in Hudson Valley</h2>

    <h3>1. Blacklegged Tick (Deer Tick) - THE DANGEROUS ONE</h3>

    <p><strong>Scientific name:</strong> Ixodes scapularis</p>

    <p><strong>Why it's dangerous:</strong> This is the primary Lyme disease vector. It can also transmit anaplasmosis and babesiosis.</p>

    <p><strong>What it looks like:</strong></p>
    <ul>
        <li>Very small - nymph stage is the size of a poppy seed</li>
        <li>Adult females are about 3-5mm (sesame seed size)</li>
        <li>Dark reddish-brown body</li>
        <li>Black legs (hence the name)</li>
        <li>Oval-shaped body</li>
    </ul>

    <p><strong>When to find them:</strong> Spring (March-May) and fall (September-October). Peak in May-June.</p>

    <p><strong>Where to find them:</strong> Woods, tall grass, leaf litter. Near hosts like deer, mice, birds.</p>

    <div class="highlight">
        <strong>üí° Pro Tip:</strong> The nymph stage is most dangerous because it's so small and often goes unnoticed. An unnoticed tick can feed for 24-48 hours, increasing Lyme transmission risk.
    </div>

    <h3>2. American Dog Tick - LESS DANGEROUS</h3>

    <p><strong>Scientific name:</strong> Dermacentor variabilis</p>

    <p><strong>Risk level:</strong> Can transmit Rocky Mountain spotted fever but NOT typically Lyme disease.</p>

    <p><strong>What it looks like:</strong></p>
    <ul>
        <li>Larger than blacklegged ticks (3-5mm before feeding)</li>
        <li>Brown body with white or gray markings (ornate pattern)</li>
        <li>Visible to the naked eye</li>
        <li>Oval-shaped</li>
    </ul>

    <p><strong>When and where:</strong> Spring-summer. Commonly found on dogs (hence the name) and in grasslands and roadsides.</p>

    <h3>3. Lone Star Tick - MODERATE CONCERN</h3>

    <p><strong>Scientific name:</strong> Amblyomma americanum</p>

    <p><strong>Risk level:</strong> Can transmit ehrlichiosis and other diseases. Not typically Lyme, but still concerning.</p>

    <p><strong>What it looks like:</strong></p>
    <ul>
        <li>Brown body</li>
        <li>Distinctive white spot on female's back</li>
        <li>About 3-5mm</li>
        <li>Found less commonly in Hudson Valley (more common in the South)</li>
    </ul>
</section>

