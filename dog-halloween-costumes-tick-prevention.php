    <section class="section" id="costume-ideas">
        <h2>Dog Halloween Costume Ideas That Work with Tick Prevention</h2>
        
        <h3>Light, Visible Costumes (Best for Tick Prevention)</h3>
        
        <p><strong>Bandana or Collar Costumes:</strong> Add a themed bandana around your dog's neck (pumpkin print, spider web, ghost faces) or a festive collar. These are barely noticeable from a tick prevention perspective and allow full visibility of your dog's body. The bandana is easy to remove for tick checks.</p>
        
        <p><strong>Vest-Style Costumes:</strong> Lightweight vests that fit over your dog's back without restricting movement. These cover the most visible area (where owners look most often) while keeping the dog's sides and chest visible for tick checks. Think Halloween-themed vests or superhero capes.</p>
        
        <p><strong>Hat or Headpiece Costumes:</strong> Put a small hat, crown, or antlers on your dog's head. These are adorable, completely non-restrictive, and keep your dog's entire body visible for tick monitoring. Think pumpkin head, reindeer ears, or a witch hat.</p>
        
        <p><strong>Bow Tie or Formal Collar Costumes:</strong> Dress your dog up as a fancy guest with a large bow tie or formal collar. This is minimalist costume fashion that's perfect for tick prevention.</p>
        
        <h3>Full-Coverage Costumes That Allow Tick Checks (Second-Best Option)</h3>
        
        <p>If you want a full-coverage costume for your dog, choose ones that are:</p>
        
        <ul>
            <li><strong>Loose-fitting:</strong> Don't choose costumes that are skin-tight. Your dog needs to move freely and you need to be able to slip your hand underneath to feel for ticks</li>
            <li><strong>See-through panels:</strong> Some costumes have mesh or see-through sections where you can see your dog's fur</li>
            <li><strong>Removable in sections:</strong> Choose costumes where you can roll up the sleeves or remove legs one at a time to check for ticks</li>
            <li><strong>Quick to remove:</strong> Avoid costumes with many buttons, snaps, or complex closures. You need to be able to remove it quickly if your dog is stressed or if you spot a tick</li>
        </ul>
        
        <h3>Costumes to Avoid</h3>
        
        <ul>
            <li>Skin-tight costumes that prevent you from feeling your dog's body</li>
            <li>Costumes that cover your dog's face or mouth (safety hazard and prevents panting)</li>
            <li>Heavy, thick costumes that could cause overheating</li>
            <li>Costumes with small parts your dog could chew off and choke on</li>
            <li>Any costume your dog shows fear or anxiety about</li>
        </ul>
