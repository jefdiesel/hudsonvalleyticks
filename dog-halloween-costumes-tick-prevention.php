    <section class="section" id="prevention">
        <h2>Complete Tick Prevention Strategy for Halloween Dogs</h2>
        
        <h3>Before Halloween Night</h3>
        
        <ul>
            <li><strong>Confirm tick prevention is active:</strong> Make sure your dog's monthly tick preventative (Simparica, Bravecto, etc.) is current. If using a collar, verify it's properly fitted. Don't let Halloween prevent you from maintaining prevention</li>
            <li><strong>Plan costume fit:</strong> Ensure the costume is loose enough that you can slip your hand underneath to feel for ticks</li>
            <li><strong>Do a practice run:</strong> Have your dog wear the costume at home for 30 minutes before Halloween to ensure they're comfortable</li>
            <li><strong>Set tick-check schedule:</strong> Plan to check your dog for ticks every 15-30 minutes while in costume (more frequently than usual)</li>
        </ul>
        
        <h3>During Halloween</h3>
        
        <ul>
            <li><strong>Frequent tick checks:</strong> Every 15-30 minutes, briefly remove your dog's costume and do a quick tick inspection. Look at neck, behind ears, between toes, groin area, and belly</li>
            <li><strong>Avoid high-tick areas:</strong> Skip visiting yards with tall grass, dense brush, or wooded areas if possible. Stick to well-maintained neighborhoods for trick-or-treating</li>
            <li><strong>Watch for scratching:</strong> If your dog excessively scratches at the costume, remove it immediately and check for ticks. Don't assume scratching is just costume discomfort</li>
            <li><strong>Limit duration:</strong> Consider a shorter trick-or-treating session (30-60 minutes) rather than hours of exposure. Your dog can go with you partway, then stay home with a caregiver</li>
            <li><strong>Keep costume removable:</strong> Never be in a situation where you can't quickly remove your dog's costume if needed</li>
        </ul>
        
        <h3>After Halloween</h3>
        
        <ul>
            <li><strong>Immediate tick check:</strong> As soon as you get home, remove the costume and do a thorough full-body tick inspection</li>
            <li><strong>Pay special attention to:</strong> Ears, between toes, neck, armpits, groin, belly, and back legs</li>
            <li><strong>Remove any ticks:</strong> Use fine-tipped tweezers to grasp the tick at the skin surface and pull straight out. Don't twist or crush the tick body</li>
            <li><strong>Consider medicated shampoo:</strong> If you found multiple ticks or want extra assurance, use a medicated flea and tick shampoo that evening</li>
            <li><strong>Monitor for days:</strong> Watch your dog over the next 2-4 weeks for any signs of Lyme disease: fever, lameness, lethargy, loss of appetite, or joint swelling</li>
        </ul>
