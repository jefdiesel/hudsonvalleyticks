        <p>Orchards involve walking through vegetation, brushing against branches, and spending extended time in potential tick habitat. Columbia County's early fall tick season (September‚ÄìOctober) remains active.</p>

        <h3>Pre-Visit Preparation</h3>
        <p>Dress children in light-colored clothing (white or khaki pants), long sleeves, and closed-toe shoes. Apply permethrin spray to clothing the night before your visit‚Äîit's safer outdoors and allows proper drying. Choose permethrin-treated clothing like Insect Shield products for maximum protection without additional application.</p>

        <h3>During Your Visit</h3>
        <p>Inspect children every 30 minutes, especially after they've been picking from lower branches or playing on the ground. Pay special attention to sock lines, behind ears, in hair, and in skin folds. Tuck pants into socks and tuck shirts in at the waist to create barriers against crawling ticks.</p>

        <h3>Post-Visit Protocol</h3>
        <p>Change clothes immediately upon arriving home. Wash all clothing worn to the orchard in hot water. Inspect children thoroughly‚Äîthe CDC recommends a full-body tick check. Use a handheld mirror to inspect hard-to-see areas. Shower within 2 hours of returning home if possible.</p>

        <div class="warning">
            <strong>Important:</strong> Ticks in Columbia County can carry Lyme disease (Borrelia burgdorferi). Early removal within 24 hours significantly reduces disease transmission risk. If you find a tick, use a fine-point tweezers to grasp close to the skin and pull steadily upward‚Äîavoid crushing the tick or using petroleum jelly or heat.
