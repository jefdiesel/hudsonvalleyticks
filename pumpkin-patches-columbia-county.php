        <h2>Tick Prevention in Pumpkin Patches</h2>
        <p>Pumpkin patches involve walking through vegetation, brushing against vines, and extended time in potential tick habitat. Columbia County's October tick population remains active.</p>

        <h3>Pre-Visit Preparation</h3>
        <p>Dress children in light-colored long pants, long sleeves, and closed-toe shoes or boots. Treat clothing with permethrin the night before your visit, or choose permethrin-treated clothing like Insect Shield products. The goal is maximum skin coverage without appearing overly formal.</p>

        <h3>During Your Visit</h3>
        <p>Perform visual tick checks every 45‚Äì60 minutes. Pay special attention to sock lines (ticks love the transition zone), behind ears, in hair, and in skin folds. Most ticks are found on lower legs and ankles. Tuck pants into socks for additional protection.</p>

        <h3>Post-Visit Protocol</h3>
        <p>Change clothes immediately upon arriving home. Wash all clothing worn to the pumpkin patch in hot water. Inspect children thoroughly with a handheld mirror, checking hard-to-see areas. Shower within 2 hours if possible.</p>

        <div class="warning">
            <strong>Important:</strong> Ticks in Columbia County can carry Lyme disease. If a tick is found, use fine-point tweezers to grasp close to the skin and pull steadily upward. Do not crush, burn, or apply substances to the tick. Save the tick in a sealed bag or take a photo for healthcare provider documentation. Watch for the "bull's-eye" rash or flu-like symptoms in following weeks.
        </div>
