                <h2>Pacing Strategies: Never Going Too Far Too Fast</h2>

                <p>The biggest risk to recovery is overdoing it too soon. This triggers a flare-up where symptoms return, setting you back days or weeks. People are surprisingly prone to this‚Äîthey feel better and push harder, then crash, and become frustrated. Understanding pacing strategies prevents this cycle.</p>

                <h3>The Energy Envelope Approach</h3>

                <p>Think of your daily energy as an "envelope" with a certain capacity. Some days the envelope is bigger; some days smaller. The principle is to use only about 70% of your available energy, keeping 30% as a buffer. This prevents crashes.</p>

                <p><strong>Example:</strong> You feel like you could go for a 2-hour hike today. Using the energy envelope principle, you aim for 1.5 hours instead, leaving capacity in reserve. The next day, you don't crash from overexertion, and you can continue progressing activity safely.</p>

                <p>This is particularly important for people with lingering post-treatment Lyme disease syndrome (PTLDS). For these patients, the difference between symptom improvement and symptom flare-up can be subtle, and small amounts of overexertion trigger disproportionate crashes.</p>

                <h3>The Two-Week Rule</h3>

                <p>Don't increase activity intensity or duration by more than 10% per week. This slow, methodical increase allows your body to adapt safely. Going slower feels frustrating when you're eager to return to normal, but it prevents setbacks that set you back weeks.</p>

                <p><strong>Example progression (hiking):</strong> Week 1‚Äî1 mile flat walk. Week 2‚Äî1.1 miles flat walk. Week 3‚Äî1.2 miles with gentle incline. Week 4‚Äî1.3 miles with hills. Week 5‚Äî1.5 miles with moderate hills. Seems slow, but after 10 weeks you're at 2+ miles on challenging terrain, and you got there without injury or flare-up.</p>

                <h3>The 50/30/20 Rule</h3>

                <p>A helpful framework for pacing is: 50% baseline activity (what you know you can do), 30% slightly challenging activity, 20% reserve. This prevents the tendency to do 100% intensity every time, which depletes your energy reserves.</p>

                <p>Some days you'll feel great and want to push to 80-90% of capacity. Try not to‚Äîbuilding that buffer prevents crashes. On average, staying in the 50-70% range keeps you safe while still progressing.</p>

                <h3>Listen to Your Body (Actually, Not Metaphorically)</h3>

                <p>Your body gives clear signals about overexertion: disproportionate fatigue, increased pain, return of previous symptoms, difficulty sleeping, elevated resting heart rate. These are objective signals you can monitor (especially with a fitness tracker).</p>

                <p>When you see these signals, don't push through‚Äîscale back your activities. A day of reduced activity now prevents a week of being sidelined later.</p>

