    <section class="section" id="removal">
        <h2>How to Remove a Tick from Your Dog</h2>
        
        <p>Despite preventative efforts, sometimes ticks attach to dogs. Proper removal is critical‚Äîincorrect removal can transmit disease or leave tick parts embedded in your dog's skin.</p>
        
        <h3>CDC-Approved Removal Method</h3>
        
        <ol>
            <li><strong>Grasp the tick with tweezers or Tick Twister as close to the skin as possible</strong> (at the attachment point, not the body)</li>
            <li><strong>Pull straight out with steady, firm pressure</strong> (don't twist or yank‚Äîthis can cause the body to rupture)</li>
            <li><strong>Place the tick in a sealed container</strong> or bag (you can save it for identification if your dog develops symptoms)</li>
            <li><strong>Clean the bite area with soap and water or antiseptic</strong></li>
            <li><strong>Dispose of the tick</strong> (flushing or placing in sealed bag are both acceptable)</li>
        </ol>
        
        <h3>What NOT To Do</h3>
        
        <ul>
            <li>Don't use tweezers to crush the tick body (this releases pathogens)</li>
            <li>Don't use petroleum jelly, essential oils, or "smothering" methods (these don't work and delay removal)</li>
            <li>Don't twist or pull too hard (tick body can rupture)</li>
            <li>Don't leave tick parts embedded in the skin</li>
            <li>Don't use your bare hands‚Äîwear gloves</li>
        </ul>
        
        <h3>After Removal</h3>
        
        <p>Monitor your dog for the next 2-4 weeks for any signs of tick-borne disease: fever, lameness, lethargy, loss of appetite, or unusual behavior. Contact your vet immediately if symptoms appear.</p>
