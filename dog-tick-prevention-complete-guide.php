        <h2>Tick-Borne Diseases Your Dog Can Contract</h2>
        
        <h3>Lyme Disease in Dogs</h3>
        
        <p>Dogs are extremely susceptible to Lyme disease. In fact, dogs contract Lyme disease at rates 10x higher than humans in endemic areas. The disease causes joint pain, lameness, fever, fatigue, and in severe cases, kidney failure.</p>
        
        <p>Many dogs with Lyme disease show no symptoms initially, making prevention even more critical. A dog can be infected for months before symptoms appear. This is why prevention is far superior to treatment: prevention stops infection before it starts.</p>
        
        <h3>Anaplasmosis</h3>
        
        <p>Transmitted by Lone Star and Blacklegged ticks, anaplasmosis causes fever, lethargy, joint pain, and vomiting in dogs. Treatment is possible but requires veterinary intervention and can be expensive.</p>
        
        <h3>Babesiosis</h3>
        
        <p>This parasitic disease affects a dog's red blood cells, causing anemia, weakness, jaundice, and potentially life-threatening complications. Prevention is critical because treatment is complex and expensive.</p>
        
        <h3>Rocky Mountain Spotted Fever</h3>
        
        <p>Though the name suggests western distribution, RMSF cases appear throughout the Northeast, including Hudson Valley. Symptoms include fever, rash, joint pain, and potential organ damage.</p>
        
