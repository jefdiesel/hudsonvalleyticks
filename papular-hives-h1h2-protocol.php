    <h2>Understanding H1 and H2 Receptors</h2>

    <h3>H1 Receptors: The Classic Allergy Mediators</h3>

    <p>
        H1 receptors are primarily located on nerve cells, smooth muscle, and endothelial cells. When histamine binds to H1 receptors, it causes:
    </p>

    <ul>
        <li>Itching and itch sensations (via nerve fiber stimulation)</li>
        <li>Smooth muscle contraction (causing bronchospasm in severe reactions)</li>
        <li>Vasodilation and increased vascular permeability (swelling)</li>
