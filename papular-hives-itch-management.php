    <h2>Cold Therapy: The Fastest Itch Relief</h2>

    <p>
        Cold application is among the most effective immediate itch relief strategies. When you apply cold to itching skin, you activate a neurological phenomenon called "pain gate control." Cold stimulation activates different nerve fibers (A-delta fibers) that literally block the itch signal (C-fiber signals) from reaching the brain.
    </p>

    <h3>How to Apply Cold Therapy</h3>

    <ul>
        <li><strong>Cold compresses:</strong> Apply wet, cold cloths to affected areas for 10-15 minutes at a time. Reapply every 1-2 hours as needed.</li>
        <li><strong>Ice packs:</strong> Never apply ice directly to skin (risk of ice burn). Wrap in a thin cloth and apply for 10-15 minutes. Reusable gel packs work well.</li>
        <li><strong>Cold water immersion:</strong> For widespread hives, cool (not cold) baths can provide relief. Water temperature 60-70¬∞F (16-21¬∞C) provides itch relief without the shock of truly cold water.</li>
        <li><strong>Frozen water bottles:</strong> Roll over affected areas (through thin cloth) for localized relief.</li>
        <li><strong>Frozen spoons:</strong> Place spoons in freezer, then gently roll over hives for precise application.</li>
    </ul>

